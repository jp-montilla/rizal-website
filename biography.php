                        <a   class="timeline-content"  data-aos="fade-right">
                            <span class="timeline-year">1872-1882</span>
                            <div class="content">
                                <h3 class="title">SECOND PERIOD</h3>
                                <p class="description">José Rizal Mercado attended the Ateneo Municipal de Manila at the age of 11, graduating at the age of 16 with highest honors. He took a post-graduate course there in land surveying.</p>
                                <p class="description">It was the period when Frs. Gomez, Burgos, and Zamora were unjustly executed by the Spanish government. Their martyrdom led Rizal to be awakened to the abuses of the regime and at the same time led him to devote himself in the future to avenge the victims of injustices and cruelties of the Spanish colonizers.</p>
                                <p class="description">Rizal Mercado completed his surveyor's training in 1877, and passed the licensing exam in May 1878, but could not receive a license to practice because he was only 17 years old. (He was granted a license in 1881 when he reached the age of majority.)</p>
                                <p class="description">In 1878, the young man also enrolled in the University of Santo Tomas as a medical student. He later quit the school, alleging discrimination against Filipino students by the Dominican professors.</p>
                            </div>
                        </a>
                    </div>
                    <div class="timeline">
                        <a   class="timeline-content"  data-aos="fade-left">
                            <span class="timeline-year">1882-1892</span>
                            <div class="content">
                                <h3 class="title">THIRD PERIOD</h3>
                                <p class="description">In May of 1882, José Rizal got on a ship to Spain without informing his parents of his intentions. He enrolled at the Universidad Central de Madrid. In June of 1884, he received his medical degree at the age of 23; the following year, he also graduated from the Philosophy and Letters department.</p>
                                <p class="description">Inspired by his mother's advancing blindness, Rizal next went to the University of Paris and then the University of Heidelberg to complete further study in the field of ophthalmology. At Heidelberg, he studied under the famed professor Otto Becker. Rizal finished his second doctorate at Heidelberg in 1887.</p>
                                <p class="description">Jose Rizal lived in Europe for 10 years. During that time, he picked up a number of languages; in fact, he could converse in more than 10 different tongues. While in Europe, the young Filipino impressed everyone who met him with his charm, his intelligence, and his mastery of an incredible range of different fields of study. Rizal excelled at martial arts, fencing, sculpture, painting, teaching, anthropology, and journalism, among other things.</p>
                                <p class="description">During his European sojourn, he also began to write novels. Rizal finished his first book, Noli Me Tangere, while living in Wilhemsfeld with the Reverend Karl Ullmer.</p>
                                <p class="description">Rizal wrote Noli Me Tangere in Spanish; it was published in 1887 in Berlin. The novel is a scathing indictment of the Catholic Church and Spanish colonial rule in the Philippines, and its publication cemented Jose Rizal's position on the Spanish colonial government's list of troublemakers. When Rizal returned home for a visit, he received a summons from the Governor General and had to defend himself from charges of disseminating subversive ideas.</p>
                                <p class="description">Although the Spanish governor accepted Rizal's explanations, the Catholic Church was less willing to forgive. In 1891, Rizal published a sequel, titled El Filibusterismo.</p>
                                <p class="description">Both in his novels and in newspaper editorials, Jose Rizal called for a number of reforms of the Spanish colonial system in the Philippines. He advocated freedom of speech and assembly, equal rights before the law for Filipinos, and Filipino priests in place of the often-corrupt Spanish churchmen. In addition, Rizal called for the Philippines to become a province of Spain, with representation in the Spanish legislature (the Cortes Generales).</p>
                                <p class="description">Rizal never called for independence for the Philippines. Nonetheless, the colonial government considered him a dangerous radical and declared him an enemy of the state.</p>
                            </div>
                        </a>
