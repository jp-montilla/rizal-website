                            <span class="timeline-year">1892-1896</span>
                            <div class="content">
                                <h3 class="title">FOURTH PERIOD</h3>
                                <p class="description">In 1892, Rizal returned to the Philippines. He was almost immediately accused of being involved in the brewing rebellion and was exiled to Dapitan City, on the island of Mindanao. Rizal would stay there for four years, teaching school and encouraging agricultural reforms.</p>
                                <p class="description">During that same period, the people of the Philippines grew more eager to revolt against the Spanish colonial presence. Inspired in part by Rizal's organization, La Liga, rebel leaders like Andres Bonifacio began to press for military action against the Spanish regime.</p>
                                <p class="description">In Dapitan, Rizal met and fell in love with Josephine Bracken, who brought her stepfather to him for a cataract operation. The couple applied for a marriage license but were denied by the Church (which had excommunicated Rizal).</p>
                                <p class="description">The Philippine Revolution broke out in 1896. Rizal denounced the violence and received permission to travel to Cuba in order to tend victims of yellow fever in exchange for his freedom. Bonifacio and two associates sneaked aboard the ship to Cuba before it left the Philippines, trying to convince Rizal to escape with them, but Rizal refused.</p>
                                <p class="description">He was arrested by the Spanish on the way, taken to Barcelona, and then extradited to Manila for trial. José Rizal was tried by court-martial, charged with conspiracy, sedition, and rebellion. Despite a lack of any evidence of his complicity in the Revolution, Rizal was convicted on all counts and given the death sentence.</p>
                                <p class="description">He was allowed to marry Josephine two hours before his execution by firing squad on December 30, 1896. Jose Rizal was just 35 years old.</p>
                            </div>
                        </a>
