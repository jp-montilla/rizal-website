                            <span class="timeline-year">1861-1872</span>
                            <div class="content">
                                <h3 class="title">FIRST PERIOD</h3>
                                <p class="description">The seventh child of Francisco Rizal Mercado and Teodora Alonzo y Quintos. The Mercado family were wealthy farmers who rented land from the Dominican religious order. Descendants of a Chinese immigrant named Domingo Lam-co, they changed their name to Mercado under the pressure of anti-Chinese feeling among the Spanish colonizers.</p>
                                <p class="description">From an early age, Jose Rizal Mercado showed a precocious intellect. He learned the alphabet from his mother at the age of three and could read and write at age five.</p>
                                <p class="description">The period when the following values and virtues were developed in him: industriousness, creativeness, rationality, and dignity.</p>
